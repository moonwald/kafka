@ContentType(EventContentType.CONNECTOR_RECEIVED)
public class ConnectorReceivedEvent extends KFCEvent {

	private Connector connectorModel;	

	private String updateType;
	
	public Connector getConnectorModel() {
		return connectorModel;
	}
	public void setConnectorModel(Connector connectorModel) {
		this.connectorModel = connectorModel;
	}

	public String getUpdateType() {
		return updateType;
	}

	public void setUpdateType(String updateType) {
		this.updateType = updateType;
	}
	@Override
	public int hashCode() {
		return HashCodeBuilder.reflectionHashCode(this, false);
	}
	
	@Override
	public boolean equals(Object obj) {
		return EqualsBuilder.reflectionEquals(this, obj, false);
	}
	
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this);
	}
}
